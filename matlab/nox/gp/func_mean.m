function [ mu ] = func_mean( x, data )
%FUNC_MEAN Summary of this function goes here
%   Detailed explanation goes here

dataMatrix = reshape(data, 24, 7, length(data)/(24*7));

redDays2009 = datetime(["2009-01-01", "2009-01-06",...
    "2009-04-10", "2009-04-13", "2009-05-01", "2009-05-21", "2009-06-06", ...
    "2009-06-19", "2009-12-24", "2009-12-25", "2009-12-31"]);
redDays2010 = datetime(["2010-01-01", "2010-01-06",...
    "2010-04-02", "2010-04-05", "2010-05-13", "2010-06-25", ...
    "2010-12-24"]);
redDays2011 = datetime(["2011-01-06", "2011-04-22",...
    "2011-04-25", "2011-06-02", "2011-06-06", "2011-06-24", ...
    "2011-12-26"]);
redDays2012 = datetime(["2012-01-06", "2012-04-06",...
    "2012-04-09", "2012-05-01", "2012-05-17", "2012-06-06", "2012-06-22", ...
    "2012-12-24", "2012-12-25", "2012-12-26", "2012-12-31"]);
redDays2013 = datetime(["2013-01-01", "2013-03-29",...
    "2013-04-01", "2013-05-01", "2013-05-09", "2013-06-06", "2013-06-21", ...
    "2013-12-24", "2013-12-25", "2013-12-26", "2013-12-31"]);
redDays2014 = datetime(["2014-01-01", "2014-01-06", "2014-04-18",...
    "2014-04-21", "2014-05-01", "2014-05-29", "2014-06-06", "2014-06-20", ...
    "2014-12-24", "2014-12-25", "2014-12-26", "2014-12-31"]);
redDays = [redDays2009, redDays2010, redDays2011, redDays2012, redDays2013, redDays2014];

mu = zeros(length(x), 1);

for j = 1:length(x)
    numHour = hour(x(j)) + 1;
    if ismember(datetime(x(j)),redDays)
        disp("Today is a red day!");
        mu(j) = mean(dataMatrix(numHour, 1 + (7 - weekday(x(j))), :));
    else
        mu(j) = mean(dataMatrix(numHour, 1, :));
    end
end


end

